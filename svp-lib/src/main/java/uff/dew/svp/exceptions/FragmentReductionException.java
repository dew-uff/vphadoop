package uff.dew.svp.exceptions;

public class FragmentReductionException extends Exception {

	public FragmentReductionException() {
		super();
	}

	public FragmentReductionException(String message) {
		super(message);
	}

	public FragmentReductionException(String message, Throwable cause) {
		super(message, cause);
	}

	public FragmentReductionException(Throwable cause) {
		super(cause);
	}

}
