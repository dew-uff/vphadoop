package uff.dew.svp.exceptions;

public class AlgebraParserException extends Exception {

	public AlgebraParserException() {
		super();
	}

	public AlgebraParserException(String message) {
		super(message);
	}

	public AlgebraParserException(String message, Throwable cause) {
		super(message, cause);
	}

	public AlgebraParserException(Throwable cause) {
		super(cause);
	}

}
